from __future__ import annotations


DEFAULT_SYSTEM_PROMPT = (
    "Ты — помощник, решающий тестовые задания и викторины. "
    "Тебе даётся текст вопроса и варианты ответов, как они распознаны с экрана. "
    "Отвечай кратко: сначала укажи букву или номер правильного варианта "
    "(например, 'A' или '3'), затем в одной-двух фразах коротко обоснуй выбор. "
    "Если структура вариантов распознана плохо, постарайся аккуратно восстановить её."
)


def build_quiz_prompt(ocr_text: str) -> str:
    """
    Формирует пользовательский промпт для LLM на основе сырых OCR-данных.
    Пока что передаём весь текст целиком.
    """
    return (
        "Ниже приведён текст вопроса теста и варианты ответов, "
        "распознанные с экрана с помощью OCR. "
        "Определи наиболее вероятно правильный вариант.\n\n"
        "Текст (как есть после OCR):\n"
        "---------------------------\n"
        f"{ocr_text}\n"
        "---------------------------\n\n"
        "Ответь в формате: БУКВА_ИЛИ_НОМЕР - краткое объяснение."
    )


__all__ = ["DEFAULT_SYSTEM_PROMPT", "build_quiz_prompt"]

